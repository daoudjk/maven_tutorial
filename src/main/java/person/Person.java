package person;

public class Person {
    private int height = 0, weight = 0;

    public int getHeight(){
        return this.height;
    }
    public void setHeight(int value)
    {
        this.height = value;
    }
    public int getWeight(){
        return this.weight;
    }
    public void setWeight(int value)
    {
        this.weight = value;
    }
}
